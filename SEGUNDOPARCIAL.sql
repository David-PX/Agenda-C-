USE AGENDA
go
create table CONTACTOS(
ID int IDENTITY(1,1) NOT NULL primary key,
NOMBRES NVARCHAR(30) NOT NULL,
APELLIDOS NVARCHAR(40) NOT NULL,
CEDULA NVARCHAR(30) NOT NULL,
CORREO NVARCHAR(40),
TELEFONO_1 NVARCHAR(25) NOT NULL,
TELEFONO_2 NVARCHAR(25),
DIRECCION nvarchar(123) null
)
